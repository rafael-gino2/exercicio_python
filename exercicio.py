#Calculador de média de notas
#Int = Numero inteiro, Float = Numeros inteiros e nn inteiros

notaUm = input('Nota um:')
notaDois = input('Nota dois:')
notaTres = input('Nota três:')
notaQuatro = input('Nota Quatro:')
resultado = (int (notaUm) + int(notaDois) + int(notaTres) + int(notaQuatro)) / 4
print(resultado)